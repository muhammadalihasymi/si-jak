
<h2>SELAYANG PANDANG</h2>
<p><strong>Daftar Transaksi Harian Belanja Daerah yang selanjutnya disingkat DTH</strong> adalah daftar yang dibuat oleh Bendahara Pengeluaran SKPD Kuasa BUD yang memuat rincian transaksi harian belanja daerah per Surat Perintah Membayar/Surat Penyediaan Dana (SPM/SPD) dan Surat Perintah Pencairan Dana (SP2D).
Rekapitulasi Transaksi Harian Belanja Daerah.

<br><strong>Rekapitulasi Transaksi Harian Belanja Daerah RTH</strong> adalah daftar yang dibuat oleh Kuasa BUD yang memuat rekapitulasi dari DTH dalam satu wilayah Provinsi/Kabupaten/Kota.

<p><br><strong>Kantor Pelayanan Pajak yang selanjutnya disingkat KPP</Strong> adalah instansi vertikal Direktorat Jenderal Pajak yang berada di bawah dan bertanggung jawab langsung kepada Kepala Kantor Wilayah Direktorat Jenderal Pajak.
Surat Setoran Pajak yang selanjutnya disingkat SSP adalah bukti pembayaran atau penyetoran pajak yang telah dilakukan dengan menggunakan formulir atau telah dilakukan dengan cara lain ke kas Negara melalui tempat pembayaran yang ditunjuk oleh Menteri Keuangan.</p>
 </P>
<p>Pengawasan terhadap pemotongan/pemungutan dan penyetoran pajak yang dilakukan oleh Bendahara Pengeluaran SKPD/Kuasa BUD meliputi:

<br>1 Perhitungan potensi pajak atas Belanja Daerah;
<br>2 Penerimaan dan penatausahaan DTH dan RTH;
<br>3 Pengujian kebenaran perhitungan dan penyetoran pajak;
<br>4 Konfirmasi kebenaran perhitungan dan penyetoran pajak; dan
<br>5 Pemeriksaan/verifikasi atas perhitungan dan penyetoran pajak.

