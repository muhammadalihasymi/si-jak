<p><strong>Pajak Penghasilan Pasal 21</strong>
<br>PPh pasal 21 adalah pemotongan pajak untuk penghasilan dari pekerjaan, jasa, atau kegiatan dengan nama dan dalam bentuk apapun yang diterima atau diperoleh wajib pajak orang pribadi dalam negeri.

Pemotongan PPh 21 dilakukan oleh:

<br>a. Pemberi kerja yang membayar gaji, upah, honorarium, tunjangan, dan pembayaran lain sebagai imbalan dari pekerjaan yang dilakukan sebagai pegawai atau bukan pegawai.

<br>b. Bendahara pemerintah yang membayar gaji, upah, honorarium, tunjangan, dan pembayaran lain yang berkaitan dengan pekerjaan, jasa, atau kegiatan.

<br>c. Dana pensiun atau badan lain yang membayarkan uang pensiun dan pembayaran lain dalam rangka masa pensiun anggota yang ikut serta program dana pension.

<br>d. Badan yang membayar honorarium atau pembayaran lain sebagai imbalan sehubungan dengan jasa termasuk jasa tenaga ahli yang melakukan pekerjaan bebas dan

<br>e. Penyelenggaran kegiatan yang melakukan pembayaran untuk pelaksanaan kegiatannya.
</br></p>
<p><strong>Pajak Penghasilan Pasal 22</strong>
<br>PPh pasal 22 adalah pajak penghasilan yang dikenakan kepada badan usaha tertentu, baik milik pemerintah maupun swasta yang melakukan kegiatan perdagangan ekspor, impor dan re-impor.

Beberapa kegiatan yang masuk dalam jenis pajak penghasilan pasal 22 adalah penyerahan barang; kegiatan di bidang impor atau kegiatan usaha di bidang lain; dan pembelian barang yang tergolong sangat mewah.
</br>
<p><strong>Pajak Penghasilan Pasal 23</strong>
<br>PPh pasal 23 adalah adalah pungutan yang dikenakan pada penghasilan atas modal, penyerahan jasa atau hadiah dan penghargaan, selain yang telah dipotong PPh Pasal 21.

Wajib pajak PPh 23 akan dipotong sebesar 15% dari jumlah bruto atas dividen, bunga, royalti, dan hadiah atau penghargaan atau bonus. Atau dipotong sebesar 2% dari jumlah bruto atas sewa dan penghasilan lain sehubungan dengan penggunaan harta dan imbalan sehubungan dengan jasa teknis, jasa manajemen, jasa konstruksi, jasa konsultan dan jasa lain selain jasa yang telah dipotong Pajak Penghasilan Pasal 21.
</br>
<p><strong>Pajak Penghasilan Pasal 25</strong>
<br>Pajak Penghasilan Pasal 25 pembayaran berupa angsuran pajak yang berasal dari jumlah pajak penghasilan terutang menurut SPT Tahunan PPh dikurangi PPh yang dipotong atau dipungut serta PPh yang dibayar atau terutang di luar negeri yang boleh dikreditkan.
</br>
<p><strong>Pajak Penghasilan Pasal 26</strong>
<br>Jenis pajak penghasilan ini dikenakan atas penghasilan yang bersumber dari Indonesia yang diterima atau diperoleh wajib pajak luar negeri selain bentuk usaha tetap di Indonesia.

Berikut ini jenis penghasilan yang dikenai PPh pasal 26:

Dividen,
Bunga
Diskonto
Imbalan sehubungan dengan jaminan pengembalian utang
Royalti,sewa
Penghasilan lain sehubungan dengan penggunaan harta
Imbalan sehubungan dengan jasa, pekerjaan, kegiatan, hadiah dan penghargaan, pensiun dan pembayaran berkala lainnya
Premi swap dan transaksi lindung lainnya.
Besaran potongan pajak jenis pajak penghasilan ini yaitu sebesar 20%.
Pajak Penghasilan Pasal 29
PPh pasal 29 adalah pajak yang harus dibayar oleh wajib pajak baik WP Orang Pribadi dan/atau WP badan sebagai akibat PPh terutang dalam SPT tahunan PPh lebih besar dari pada kredit pajak yang telah dipotong atau dipungut pihak lain dan yang sudah disetor sendiri.

Wajib pajak harus melunasi kekurangan pembayaran pajak terutang sebelum SPT PPh yang baru untuk tahun berjalan disampaikan.
</br>
Pajak Penghasilan Pasal 4 ayat (2)
<br>PPh pasal 4 ayat (2) adalah pajak atas penghasilan yang bersifat final serta tidak dapat dikreditkan dengan pajak penghasilan terutang.

Objek pajak penghasilan pasal 4 ayat (2) adalah investasi, atau simpanan seperti bunga deposito, bunga obligasi dan surat utang negara, bunga simpanan yang dibayarkan koperasi, hadiah undian, transaksi saham dan sekuritas lainnya, serta transaksi-transaksi lainnya yang menguntungkan sebagaimana diatur dalam undang-undang.</br>